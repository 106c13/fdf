#include "fdf.h"

int	ft_htod(char **str)
{
	int	num;

	num = 0;
	(*str) += 3;
	while (**str && **str != ' ' && **str != '\n')
	{
		if (**str >= '0' && **str <= '9')
			num = num * 16 + **str - '0';
		else if (**str >= 'a' && **str <= 'f')
			num = num * 16 + **str - 'a' + 10;
		else if (**str >= 'A' && **str <= 'F')
			num = num * 16 + **str - 'A' + 10;
		(*str)++;
	}
	return (num);
}
