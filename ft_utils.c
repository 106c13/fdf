#include "fdf.h"

int	ft_isnum(char *str)
{
	if (!(*str) || !(*(str + 1)))
		return (NULL);
	if (*str == '-')
		str++;
	while (*str)
	{
		if (*str < '0' || *str > '9')
			return (0);
		str++;
	}
	return (1);
}
